	f_[6] = +reaction_12+reaction_23+reaction_62;
	f_[7] = -reaction_14-reaction_23-reaction_76-reaction_58-reaction_1+reaction_2;
	f_[8] = +reaction_78-reaction_76+reaction_3_7;
	f_[9] = +reaction_56+reaction_76-reaction_62;
