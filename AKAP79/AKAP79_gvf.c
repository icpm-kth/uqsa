	f_[11] = +reaction_44_-reaction_4_8;
	f_[12] = +reaction_33_-reaction_3_7;
	f_[13] = -reaction_1;
	f_[14] = +reaction_1-reaction_2;
	f_[15] = +reaction_2;
	return GSL_SUCCESS;
